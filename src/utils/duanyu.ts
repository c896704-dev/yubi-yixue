/**
 * 根据卦名获取断语
 * @param hexagramName 卦名（如"乾为天"、"天地否"）
 * @returns 断语条目，未找到返回 null
 */
export function getDuanYu(hexagramName: string): DuanYuEntry | null {
  return DUAN_YU_TABLE[hexagramName] || null
}

/**
 * 获取所有支持断语的卦名列表
 */
export function getSupportedHexagramNames(): string[] {
  return Object.keys(DUAN_YU_TABLE)
}

/**
 * 根据卦名获取指定维度的断语
 * @param hexagramName 卦名
 * @param dimension 维度名称
 * @returns 断语文案，未找到返回 null
 */
export function getDuanYuByDimension(
  hexagramName: string,
  dimension: keyof DuanYuEntry,
): string | null {
  const entry = DUAN_YU_TABLE[hexagramName]
  if (!entry) return null
  return entry[dimension] || null
}

export default DUAN_YU_TABLE
