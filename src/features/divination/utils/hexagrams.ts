/** 按卦名查找 */
export function getHexagramByName(name: string): Hexagram | undefined {
  return hexagrams.find(h => h.name === name)
}

/** 按上下卦查找 */
export function getHexagramByTrigrams(upper: string, lower: string): Hexagram | undefined {
  return hexagrams.find(h => h.upperTrigram === upper && h.lowerTrigram === lower)
}

/** 按卦画符号查找 */
export function getHexagramBySymbol(symbol: number): Hexagram | undefined {
  return hexagrams.find(h => h.symbol === symbol)
}

/** 按宫位获取八卦 */
export function getHexagramsByPalace(palace: string): Hexagram[] {
  return hexagrams.filter(h => h.palace === palace)
}

/** 三画卦比特值 → 八卦数（用于 getTrigramByNumber） */
function bitValToNumber(bits: number): number {
  return bits === 0 ? 8 : 8 - bits
}

/** 从6爻（0=阴1=阳，index0=初爻）推算卦画符号并查找卦名 */
export function getHexagramFromLines(lines: number[]): Hexagram | undefined {
  const lower = (lines[0] || 0) | ((lines[1] || 0) << 1) | ((lines[2] || 0) << 2)
  const upper = (lines[3] || 0) | ((lines[4] || 0) << 1) | ((lines[5] || 0) << 2)
  const symbol = (upper << 3) | lower
  return getHexagramBySymbol(symbol)
}

export default hexagrams
